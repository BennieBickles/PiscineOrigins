#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	a;

	a = -1;
	if (str[0] == 0)
		return (1);
	while (str[++a])
	{
		if (str[a] < '0' || str[a] > '9')
			return (0);
	}
	return (1);
}
