int	ft_str_is_uppercase(char *str)
{
	int	a;

	a = -1;
	if (str[0] == 0)
		return (1);
	while (str[++a])
	{
		if (str[a] < 'A' || str[a] > 'Z')
			return (0);
	}
	return (1);
}
