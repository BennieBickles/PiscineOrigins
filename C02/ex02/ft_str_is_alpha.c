#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	a;

	a = -1;
	if (str[0] == 0)
		return (1);
	while (str[++a])
	{
		if ((str[a] > 'Z' && str[a] < 'a') || str[a] < 'A' || str[a] > 'z')
			return (0);
	}
	return (1);
}
