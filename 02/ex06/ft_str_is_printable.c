int	ft_str_is_printable(char *str)
{
	int	a;

	a = -1;
	if (str[0] == 0)
		return (1);
	while (str[++a])
	{
		if (str[a] < ' ' || str[a] > 126)
			return (0);
	}
	return (1);
}
