#include <unistd.h>

int	main(int argc, char **argv)
{
	int	b;
	int	c;

	b = 1;
	c = 0;
	if (argc > 1)
	{
		while (b < argc)
		{
			c = 0;
			while (argv[b][c] != '\0')
			{
				write(1, &argv[b][c], 1);
				c++;
			}
			write(1, "\n", 1);
			b++;
		}
	}
	return (0);
}
